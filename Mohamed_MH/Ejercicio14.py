# Enunciado:  Ejercicio 14: Dado un número de dos cifras, diseñe un algoritmo que permita obtener el número invertido. Ejemplo, si se introduce 23 que muestre 32.


numero = input("Ingresa un número: ")


numero_invertido = numero[::-1]


print(f"El número invertido es: {numero_invertido}")


