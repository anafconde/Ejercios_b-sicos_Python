#Calcular el perímetro y área de un rectángulo dada su base y su altura

#Calcular el perímetro
print("Calcular el perímetro de un rectángulo")
base=float(input("Introduce el valor de la base: "))
altura=float(input("Introduce el valor de la altura: "))
per=(base*2)*(altura*2)
print("El perímetro de su rectángulo es: ", per)

# Calcular el área
print("Calcular el área de un rectángulo")
base=float(input("Introduce el valor de la base: "))
altura=float(input("Introduce el valor de la altura: "))
area=base*altura
print("El área de su rectángulo es: ",area)