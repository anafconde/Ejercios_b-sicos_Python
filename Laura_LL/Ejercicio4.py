#Dados dos números, mostrar la suma, resta, división y multiplicación de ambos

n1=float(input("Introduce el primer número: "))
n2=float(input("Introduce el segundo número: "))

suma=n1+n2
print("La suma de los números: ",n1, " y ",n2," es: ",suma)

resta=n1-n2
print("La resta de los números: ",n1, " y ",n2," es: ",resta)

div=n1/n2
print("La división de los números: ",n1, " y ",n2," es: ",div)

mult=n1*n2
print("La multiplicación de los números: ",n1, " y ",n2," es: ",mult)