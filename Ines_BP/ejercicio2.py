#Calcular el perímetro y área de un rectángulo dada su base y su altura.
base=float(input("Introduce la base: "))
altura=float(input("Introduce la altura: "))
per=((base*2)+(altura*2))
area=base*altura
print(f"El perímetro es: {per}")
print(f"El área es: {area}")
